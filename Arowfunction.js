// Arowfunction
const sum = a => a * a
console.log(sum(4));

const add = (a, b) => a + b
console.log(add(3, 5));


const subtract = (a, b) => a - b
console.log(subtract(10, 4));


const multiply = (a, b) => a * b
console.log(multiply(2, 6));


const divide = (a, b) => a / b
console.log(divide(20, 4));
